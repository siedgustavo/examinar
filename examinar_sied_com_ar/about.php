                <h2>ESCUELA Nº 49 J.J. de URQUIZA</h2>
					<p>La Carrera de Analista de Sistemas de Computación de Nivel Terciario fue creada en la Escuela de Comercio Nº 49 "Cap. Gral J.J.de Urquiza", en el año 1986, dependiendo de la Dirección Nacional de Educación Superior, del Ministerio de Educación y Justicia de la Nación.</p>
					<p>Su creación permitió elevar el establecimiento a la categoría de Escuela Superior, y contó con dos divisiones de primer año en su inicio.</p>
					<p>Los contenidos de la Carrera estaban determinados por el plan RM Nº 1823/83, el cual establecía una duración de tres años para obtener el título de Analista de Sistemas de Computación, con un título intermedio de Analista Programador, al aprobar segundo año.</p>
					<p>&nbsp;</p>
					<p>La carga horaria del Plan Nº 1823/83, era de 62hs distribuidas en veinte para primer año veinte para segundo y veintidós para tercero.</p>
					<p>En el año 1989, se elevó a la Dirección Nacional un proyecto para actualizar los contenidos de la Carrera. Este mantiene la duración, pero cambia la distribución horaria incrementando el total a 69 horas e introduce cambios necesarios para ajustar las materias a las necesidades de una permanente actualización. El proyecto fue aprobado por la Resolución Ministerial 1066/90. El primero de abril del año 1994 la escuela fue transferida, junto con otros establecimientos, a la jurisdicción provincial. Hasta la fecha no se ha modificado su estructura curricular ni su sistema de promoción.</p>
					<p>A partir de entonces, nuestra Carrera, se convirtió en referente para otros establecimientos similares, asesorando y ayudando en la afirmación de las disciplinas informáticas en el amplio espectro de la Educación.</p>
					<p>En el año 2015 el Ministerio de Educación de la Provincia de Santa fe nos elige para la creación de dos nuevas carreras: Analista Programador e Informática y Redes de Datos en el marco de una política que atiende al Plan Estratégico Provincial.</p>
					<p>Estas carreras tendrán a partir del 2106 marcos de referencias aprobados por el Consejo Federal de Educación, cambiando su denominación a: Analista Programador como Desarrollador de Software y la carrera Informática y Redes de Datos como Soporte de Infraestructura de Tecnologías de Información.</p>
					<p>Ambas carreras nuevas se dictarán dentro del Área Tecnológica de Rosario, donde se ubican las principales empresas de TI conformando el Polo Tecnológico de Rosario. El lugar físico de dictado de ambas carreras se encuentran en construcción y representarán un edificio nuevo dentro del espacio ocupado por el ex Batallón 121.</p>
					<p>Este nuevo desafío representa un estímulo para nuestra institución cuya trayectoria exitosa viene marcando un indudable lugar en el mercado laboral regional.</p>
